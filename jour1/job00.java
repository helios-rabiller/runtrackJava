public static void main(String[] args) {
    char lettre = 'A';
    String texte = "Bonjour";
    int entier = 10;
    long grandEntier = 10000000000L;
    float reel = 3.14f;
    boolean booleen = true;

    System.out.println("char : " + lettre);
    System.out.println("string : " + texte);
    System.out.println("int : " + entier);
    System.out.println("long : " + grandEntier);
    System.out.println("float : " + reel);
    System.out.println("boolean : " + booleen);

    int TOTO = (int)3.817;
    System.out.println("Valeur de TOTO : " + TOTO);

}

